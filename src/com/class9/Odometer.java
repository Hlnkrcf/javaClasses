package com.class9;

public class Odometer {

	public static void main(String[] args) {

		for (int k = 0; k <= 9; k++) {

			for (int y = 0; y < 10; y++) {
				for (int n = 0; n < 10; n++) {

					for (int a = 0; a < 10; a++) {
						System.out.println(k + "" + y + "" + n + "" + a);
					}

				}

			}
		}

	}
}
